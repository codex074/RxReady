-- 005: Drug catalogue table + seed from druglist_2026-06_summary.xlsx

create table if not exists public.drugs (
  id           uuid primary key default gen_random_uuid(),
  name         text not null unique,
  generic_name text not null default '',
  strength     text not null default '',
  unit         text not null default '',
  price        numeric(10,2),
  color_tag    text,
  active       boolean not null default true,
  created_at   timestamptz not null default now(),
  updated_at   timestamptz not null default now()
);

create index if not exists idx_drugs_name on public.drugs using gin(to_tsvector('simple', name));
create index if not exists idx_drugs_generic on public.drugs using gin(to_tsvector('simple', generic_name));
create index if not exists idx_drugs_active on public.drugs(active);

-- trigger: keep updated_at current
create or replace function public.touch_drugs_updated_at()
returns trigger language plpgsql as $$
begin new.updated_at = now(); return new; end;
$$;
drop trigger if exists drugs_touch_updated_at on public.drugs;
create trigger drugs_touch_updated_at
  before update on public.drugs
  for each row execute function public.touch_drugs_updated_at();

-- RLS
alter table public.drugs enable row level security;

drop policy if exists drugs_staff_select on public.drugs;
create policy drugs_staff_select
  on public.drugs for select to authenticated
  using (public.is_active_staff());

drop policy if exists drugs_admin_insert on public.drugs;
create policy drugs_admin_insert
  on public.drugs for insert to authenticated
  with check (public.is_admin());

drop policy if exists drugs_admin_update on public.drugs;
create policy drugs_admin_update
  on public.drugs for update to authenticated
  using (public.is_admin())
  with check (public.is_admin());

drop policy if exists drugs_admin_delete on public.drugs;
create policy drugs_admin_delete
  on public.drugs for delete to authenticated
  using (public.is_admin());

revoke all on public.drugs from anon, authenticated;
grant select, insert, update, delete on public.drugs to authenticated;

